import {
  assert,
  describe,
  test,
  clearStore,
  beforeAll,
  afterAll
} from "matchstick-as/assembly/index"
import { Address } from "@graphprotocol/graph-ts"
import { Dropped } from "../generated/schema"
import { Dropped as DroppedEvent } from "../generated/EntryPoint/EntryPoint"
import { handleDropped } from "../src/entry-point"
import { createDroppedEvent } from "./entry-point-utils"

// Tests structure (matchstick-as >=0.5.0)
// https://thegraph.com/docs/en/developer/matchstick/#tests-structure-0-5-0

describe("Describe entity assertions", () => {
  beforeAll(() => {
    let account = Address.fromString(
      "0x0000000000000000000000000000000000000001"
    )
    let forEvent = Address.fromString(
      "0x0000000000000000000000000000000000000001"
    )
    let newDroppedEvent = createDroppedEvent(account, forEvent)
    handleDropped(newDroppedEvent)
  })

  afterAll(() => {
    clearStore()
  })

  // For more test scenarios, see:
  // https://thegraph.com/docs/en/developer/matchstick/#write-a-unit-test

  test("Dropped created and stored", () => {
    assert.entityCount("Dropped", 1)

    // 0xa16081f360e3847006db660bae1c6d1b2e17ec2a is the default address used in newMockEvent() function
    assert.fieldEquals(
      "Dropped",
      "0xa16081f360e3847006db660bae1c6d1b2e17ec2a-1",
      "account",
      "0x0000000000000000000000000000000000000001"
    )
    assert.fieldEquals(
      "Dropped",
      "0xa16081f360e3847006db660bae1c6d1b2e17ec2a-1",
      "forEvent",
      "0x0000000000000000000000000000000000000001"
    )

    // More assert options:
    // https://thegraph.com/docs/en/developer/matchstick/#asserts
  })
})
